--[[Définit les templates de vaisseaux utilisés dans le jeu. Chaque fichier définit une calsse de vaisseaux ou de structures.

Ces classes sont :
-Transports : Des vaisseaux lents et lourds, peu armés, conçus pour le commerce
-Escorteurs : Des vaisseaux rapides et agiles, légèrement armés, conçus pour la chasse aux pirates, la reconnaissance et l'escorte de vaisseaux plus lents.
-Frégates : Des vaisseaux polyvalents, aptes à la fois au combat et au commerce.
-Croiseurs Légers : Des vaisseaux de guerre endurants conçus pour servir de noyau aux patrouilles de vaisseaux, ils forment souvent le coeur d'un détachement.
-Croiseurs : Des vaisseaux exclusivement conçus pour la guerre, dotés d'n bon blindage et d'une bonne puissance de feu mais lourds et lents.
-Croiseurs de Combat : Des vaisseaux spécialisés équipés d'une forte puissance de feu, généralement rares.
-Grands Croiseurs : Des vaisseaux rares, antiques et redoutables, aussi lents que puissants, dotés d'une endurance et d'une puissance de feu supérieure.
-Cuirassés : Des mastodontes de guerre tenant plus de la station de combat mobile que du vaisseau, exceptionnellement rares, puissants et précieux.
-Stations : Des stations spatiales allant du simple relai de communication au Dock Spatial.
-Special : Les vaisseaux prévus pour les équipages joueurs de De Verve et D'Acier.
-Chaos : Vaisseaux du Chaos, toutes classes confondues.
-Eldar : Vaisseaux Eldars, toutes classes confondues. Ne possèdent pas de bouclier mais disposent d'une fonction de camouflage.
--]]

